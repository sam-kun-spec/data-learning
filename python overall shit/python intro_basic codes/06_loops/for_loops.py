# for loop = used to iterate over a sequence (list, tuple, string) 
#            repeat a block of code to a certain no. of times.

for i in range(10): # 0-9
    print(i)

print("done")

for i in range (2, 12):
    print(i)

print("done 2")

for i in range( 2, 18, 2): # skipping value by 2
    print(i)

print("done 3")

for i in range(10, 0 ,-1): # counting backwards 
    print(i)

print("happy new year bruh")